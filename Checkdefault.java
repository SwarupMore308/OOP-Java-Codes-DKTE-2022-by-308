class Checkdefault
{
  		static int a;
		static byte b;
		static char c;
		static short d;
		static long l;
		static double dl;
	public static void main(String[] args)
	{
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(l);
		System.out.println(dl);
	}
}